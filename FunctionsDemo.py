#User-defined function with - Arguments, No return value
def greet(name):
    print("Hello", name)
greet("Bharti1")

#User-defined function with - No arguments, With return value
def add():
    return 5+3
print(add())

#User-defined function with - Arguments, With return value
def add(a,b):
    return a+b
print(add(1,2))



